    private void jPanel2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel2MouseClicked
        // TODO add your handling code here:
        tSenha.setEnabled(false);
        tCpf.setEnabled(false);
        if (tCpf.getText().equalsIgnoreCase("") && tSenha.getPassword().length == 0) {
            tCpf.setBorder(bordaCliqueTextoFora);
            tSenha.setBorder(bordaCliqueTextoFora);
            jLabel1.setForeground(Color.decode("#7A7A7A"));
            jLabel3.setForeground(Color.decode("#7A7A7A"));
            tCpf.setEnabled(true);
            tSenha.setEnabled(true);
        } else if (tCpf.getText().equalsIgnoreCase("")) {
            tCpf.setBorder(bordaCliqueTextoFora);
            tSenha.setBorder(bordaCliqueTexto);
            jLabel1.setForeground(Color.decode("#7A7A7A"));
            jLabel3.setForeground(Color.decode("#000000"));
            tCpf.setEnabled(true);
            tSenha.setEnabled(true);
        } else if (tSenha.getPassword().length == 0) {
            tCpf.setBorder(bordaCliqueTexto);
            tSenha.setBorder(bordaCliqueTextoFora);
            jLabel3.setForeground(Color.decode("#7A7A7A"));
            jLabel1.setForeground(Color.decode("#000000"));
            tCpf.setEnabled(true);
            tSenha.setEnabled(true);
        } else {
            tCpf.setBorder(bordaCliqueTexto);
            tSenha.setBorder(bordaCliqueTexto);
            jLabel1.setForeground(Color.decode("#000000"));
            jLabel3.setForeground(Color.decode("#000000"));
            tCpf.setEnabled(true);
            tSenha.setEnabled(true);
        }
    }//GEN-LAST:event_jPanel2MouseClicked

