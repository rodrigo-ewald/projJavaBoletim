    private void bLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bLoginActionPerformed
        // TODO add your handling code here:
        AdministradorDAO aDAO = new AdministradorDAO();
        professorDAO pDAO = new professorDAO();
        String cpfMascara = JMascara.GetJmascaraLimpar(tCpf.getText());
        char[] converteSenha = tSenha.getPassword();
        String stringSenha = new String(converteSenha);
        boolean erro = false;
        if (cpfMascara.equals("")) {
            erro = true;
            JOptionPane.showMessageDialog(null, "cpf");
        } else if (stringSenha.equals("")) {
            erro = true;
            JOptionPane.showMessageDialog(null, "senha");
        } else if (radialProfessor.isSelected() == false & radialAdmin.isSelected() == false) {
            erro = true;
            JOptionPane.showMessageDialog(null, "botoes");
        }
        if (erro == true) {
            JOptionPane.showMessageDialog(null, "  Por favor!\n  Preencha todos os campos!");
            return;
        }
        if (radialProfessor.isSelected()) {
            if (pDAO.verificaLogin(cpfMascara, stringSenha)) {
                Professor p = pDAO.consultar(cpfMascara);
                new CadastrarNotas().setVisible(true);
                new Login_sucessoProfessor().setVisible(true);
                this.dispose();
            } else {
                new Login_falhaProfessor().setVisible(true);
            }
        } else if (radialAdmin.isSelected()) {
            if (aDAO.verificaLogin(cpfMascara, stringSenha)) {
                Admin a = aDAO.consultar(cpfMascara);
                new CadastrarAluno().setVisible(true);
                new Login_sucessoAdmin().setVisible(true);
                this.dispose();
            } else {
                new Login_falhaAdmin().setVisible(true);
            }
        }
    }//GEN-LAST:event_bLoginActionPerformed

