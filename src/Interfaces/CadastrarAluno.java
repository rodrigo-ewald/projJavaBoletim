    private void carregarCbTurma() {
        cbTurmaAluno.removeAllItems();
        Connection con = Conecta.iniciaConexao();
        PreparedStatement prepara = null;
        ResultSet rs = null;
        try {
            prepara = con.prepareStatement("SELECT * FROM curso");
            rs = prepara.executeQuery();
            while (rs.next()) {
                cbTurmaAluno.addItem(rs.getString("turmaCurso"));
            }
            cbTurmaAluno.setSelectedIndex(-1);
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Ocorreu um erro ao preencher turmas!");
        } finally {
            Conecta.fechaConexao(con, prepara, rs);
        }
    }

