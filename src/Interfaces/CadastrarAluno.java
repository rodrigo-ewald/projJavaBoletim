    private void bCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bCadastrarActionPerformed
        // TODO add your handling code here:
        Aluno a = new Aluno();
        AlunoDAO aDAO = new AlunoDAO();
        Object codigoTurmaObjeto = cbTurmaAluno.getSelectedItem();
        int codigoTAluno = Integer.parseInt((String) codigoTurmaObjeto);
        Object nomeTurnoObjeto = cbTurnoAluno.getSelectedItem();
        String nomeTurno = (String) nomeTurnoObjeto;
        String cpfMascaraAluno = JMascara.GetJmascaraLimpar(tCpfAluno.getText());
        char[] converteSenhaAluno = tSenhaAluno.getPassword();
        String stringSenhaAluno = new String(converteSenhaAluno);
        a.setMatriculaAluno(Integer.parseInt(tMatriculaAluno.getText()));
        a.setCpfAluno(cpfMascaraAluno);
        a.setNomeAluno(tNomeAluno.getText());
        a.setTurnoAluno(nomeTurno);
        a.setTurmaAluno(codigoTAluno);
        a.setSenhaAluno(stringSenhaAluno);
        a.setEmailAluno(tEmailAluno.getText());
        aDAO.cadastrarAluno(a);
    }//GEN-LAST:event_bCadastrarActionPerformed

