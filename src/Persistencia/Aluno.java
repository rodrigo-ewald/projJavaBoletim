package Persistencia;

/**
 *
 * @author rodri
 */
public class Aluno {
    
    private int matriculaAluno;
    private int turmaAluno;
    private String cpfAluno;
    private String nomeAluno;
    private String turnoAluno;
    private String senhaAluno;
    private String emailAluno;

    /**
     * @return the matriculaAluno
     */
    public int getMatriculaAluno() {
        return matriculaAluno;
    }

    /**
     * @param matriculaAluno the matriculaAluno to set
     */
    public void setMatriculaAluno(int matriculaAluno) {
        this.matriculaAluno = matriculaAluno;
    }

    /**
     * @return the turmaAluno
     */
    public int getTurmaAluno() {
        return turmaAluno;
    }

    /**
     * @param turmaAluno the turmaAluno to set
     */
    public void setTurmaAluno(int turmaAluno) {
        this.turmaAluno = turmaAluno;
    }

    /**
     * @return the cpfAluno
     */
    public String getCpfAluno() {
        return cpfAluno;
    }

    /**
     * @param cpfAluno the cpfAluno to set
     */
    public void setCpfAluno(String cpfAluno) {
        this.cpfAluno = cpfAluno;
    }

    /**
     * @return the nomeAluno
     */
    public String getNomeAluno() {
        return nomeAluno;
    }

    /**
     * @param nomeAluno the nomeAluno to set
     */
    public void setNomeAluno(String nomeAluno) {
        this.nomeAluno = nomeAluno;
    }

    /**
     * @return the turnoAluno
     */
    public String getTurnoAluno() {
        return turnoAluno;
    }

    /**
     * @param turnoAluno the turnoAluno to set
     */
    public void setTurnoAluno(String turnoAluno) {
        this.turnoAluno = turnoAluno;
    }

    /**
     * @return the senhaAluno
     */
    public String getSenhaAluno() {
        return senhaAluno;
    }

    /**
     * @param senhaAluno the senhaAluno to set
     */
    public void setSenhaAluno(String senhaAluno) {
        this.senhaAluno = senhaAluno;
    }

    /**
     * @return the emailAluno
     */
    public String getEmailAluno() {
        return emailAluno;
    }

    /**
     * @param emailAluno the emailAluno to set
     */
    public void setEmailAluno(String emailAluno) {
        this.emailAluno = emailAluno;
    }
            
}
